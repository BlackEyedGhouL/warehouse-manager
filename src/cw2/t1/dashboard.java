    public void createchart(){
        lblwel.setText("Welcome , "+SorL.username);
        getvaluesdb();
        int free = 100-(l+(m/2)+(s/4));
        DefaultPieDataset dataset = new DefaultPieDataset();
        dataset.setValue("Free space", new Integer(free));
        dataset.setValue("Large crates", new Integer(l));
        dataset.setValue("Medium crates", new Integer(m));
        dataset.setValue("Small crates", new Integer(s));
        JFreeChart chart = ChartFactory.createPieChart("", dataset, true, true, false);
        PiePlot p=(PiePlot)chart.getPlot();
        p.setDirection(Rotation.CLOCKWISE);
        
        ChartPanel chartPanel = new ChartPanel(chart);
        pchart.add(chartPanel);
    }
