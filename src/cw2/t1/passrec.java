    private static Message PrepareMessage(Session session, String myemail, String recepient) {
        try {
            Message message = new MimeMessage(session);
            message.setFrom(new InternetAddress(myemail));
            message.setRecipient(Message.RecipientType.TO, new InternetAddress(recepient));
            message.setSubject("WMS Verification Code");
            message.setText("Hey there, \n Your verification code : " + r);
            return message;
        } catch (MessagingException ex) {
            Logger.getLogger(MailVerification.class.getName()).log(Level.SEVERE, null, ex);
        }
        return null;
    }
    
    public static void SendMail(String recepient) throws MessagingException{
        Properties properties = new Properties();
        
        properties.put("mail.smtp.auth", "true");
        properties.put("mail.smtp.starttls.enable", "true");
        properties.put("mail.smtp.host", "smtp.gmail.com");
        properties.put("mail.smtp.port", "587");
        
        String myemail="senithumeshac@gmail.com";
        String mypassword="senithumeshac#";
        
        Session session = Session.getDefaultInstance(properties, new Authenticator() {
            @Override
            protected PasswordAuthentication getPasswordAuthentication() {
                return new PasswordAuthentication(myemail, mypassword);
            }           
});
        Message message = PrepareMessage(session,myemail,recepient);
        Transport.send(message);
    }

